//Aula sobre variáveis
#include <stdio.h>

int main() {//Chave delimita um bloco de código (INICIO DO BLOCO)
	//Declarando variáveis
	int idade = 0 ;
	
	printf("Qual aé a sua idade ? \n");
	//Reeber dados 
	scanf("%d",&idade);
	printf("A sua idade é %d ",idade);
	printf("\n");
	return 0;
}//(FIM DO BLOCO)


